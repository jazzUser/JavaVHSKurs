package c_methoden_und_zugriffsmodifikatoren.klassenMethoden;

public class MathMethoden {
    public static void main(String[] args) {
        // Maximum und Minimum
        System.out.println("Max von 10 und 20: " + Math.max(10, 20));
        System.out.println("Min von 10 und 20: " + Math.min(10, 20));

        // Quadratwurzel
        System.out.println("Wurzel aus 25: " + Math.sqrt(25));

        // Potenz
        System.out.println("2 hoch 3: " + Math.pow(2, 3));

        // Runden
        System.out.println("Runden: " + Math.round(4.6));

        // Zufallszahl zwischen 0 und 1
        System.out.println("Zufallszahl: " + Math.random());
    }
}
